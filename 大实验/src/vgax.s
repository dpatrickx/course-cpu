START:
	; initialize all address
	LI R0 0x00BF
	SLL R0 R0 0x0000 ;
	ADDIU R0 0x0004 ; R0 0xBF04
	LI R1 0x0000 ;R1 0x0000 address
	LI R2 0x0001
	SLL R2 R2 0x0000
	ADDIU R2 0x001F ; R2 0X011F address region
	LI R3 0x0000 ; R3 0x0000 value
INITIAL_LOOP:
	SW R0 R1 0x0000
	SW R0 R3 0x0001
	ADDIU R1 0x0001
	SUBU R1 R2 R4
	BNEZ R4 INITIAL_LOOP ; imm # T !=0 时 时  跳转
INITIAL_LETTER:
	; A
	LI R1 0x0004 ; R1 0x0004
 	LI R2 0x003C ; R2 0x003C
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	ADDIU R1 0x0001 ; R1 0x0005
 	LI R2 0x0024
 	SLL R2 R2 0x0000
 	LI R3 0x0024
 	OR R2 R3 ; R2 0x2424
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	ADDIU R1 0x0001 ; R1 0x0006
 	LI R2 0x007E
 	SLL R2 R2 0x0000
 	LI R3 0x0042
 	OR R2 R3 ; R2 0x7E42
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	ADDIU R1 0x0001 ; R1 0x0007
 	LI R2 0x0042
 	SLL R2 R2 0x0000 ; R2 0x4200
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	; B
 	ADDIU R1 0x0001 ; R1 0x0008
 	LI R2 0x007C ; R2 0x007C
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	ADDIU R1 0x0001 ; R1 0x0009
 	LI R2 0x0042
 	SLL R2 R2 0x0000
 	LI R3 0x007C
 	OR R2 R3 ; R2 0x427C
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	ADDIU R1 0x0001 ; R1 0x000A
 	LI R2 0x0042
 	SLL R2 R2 0x0000
 	LI R3 0x0042
 	OR R2 R3 ; R2 0x4242
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	ADDIU R1 0x0001 ; R1 0x000B
 	LI R2 0x007C
 	SLL R2 R2 0x0000 ; R2 0x7C00
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 BEGIN:
 	; LETTERS
 	LI R1 0x0001
 	SLL R1 R1 0x0000 ; R1 0x0100
 	LI R2 0x0004 ; R2 0x0004
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	ADDIU R1 0x0001 ; R1 0x0101
 	LI R2 0x0008 ; R2 0x0008
 	SW R0 R1 0x0000
 	SW R0 R2 0x0001
 	B BEGIN
 	NOP